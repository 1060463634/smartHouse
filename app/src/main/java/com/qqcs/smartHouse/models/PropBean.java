package com.qqcs.smartHouse.models;

public class PropBean {
    private String propId;
    private String deviceId;
    private String propName;
    private String propValue;
    private String propTypeCode;
    private String propType;
    private String propClass;
    private String available;
    private String steerable;
    private String propIndex;

    public String getPropId() {
        return propId;
    }

    public void setPropId(String propId) {
        this.propId = propId;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getPropName() {
        return propName;
    }

    public void setPropName(String propName) {
        this.propName = propName;
    }

    public String getPropValue() {
        return propValue;
    }

    public void setPropValue(String propValue) {
        this.propValue = propValue;
    }

    public String getPropTypeCode() {
        return propTypeCode;
    }

    public void setPropTypeCode(String propTypeCode) {
        this.propTypeCode = propTypeCode;
    }

    public String getPropType() {
        return propType;
    }

    public void setPropType(String propType) {
        this.propType = propType;
    }

    public String getPropClass() {
        return propClass;
    }

    public void setPropClass(String propClass) {
        this.propClass = propClass;
    }

    public String getAvailable() {
        return available;
    }

    public void setAvailable(String available) {
        this.available = available;
    }

    public String getSteerable() {
        return steerable;
    }

    public void setSteerable(String steerable) {
        this.steerable = steerable;
    }

    public String getPropIndex() {
        return propIndex;
    }

    public void setPropIndex(String propIndex) {
        this.propIndex = propIndex;
    }
}
